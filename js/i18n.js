// Current language
let currentLang = localStorage.getItem('mendien-lang') || 'es';

/**
 * Get translation for a key
 */
function t(key) {
  return translations[currentLang]?.[key] || translations['es'][key] || key;
}

/**
 * Apply translations to all elements with data-i18n attribute
 */
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const value = t(key);
    if (value) {
      // Check if the content contains HTML
      if (value.includes('<')) {
        el.innerHTML = value;
      } else {
        el.textContent = value;
      }
    }
  });

  // Update html lang attribute
  document.documentElement.lang = currentLang === 'eu' ? 'eu' : currentLang === 'en' ? 'en' : 'es';

  // Update active state of language buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });

  // Re-render calendar with new language
  if (typeof renderCalendar === 'function') {
    renderCalendar();
  }
}

/**
 * Set language and apply
 */
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('mendien-lang', lang);
  applyTranslations();
}

/**
 * Initialize language switcher
 */
function initLanguageSwitcher() {
  const switcher = document.getElementById('langSwitcher');
  if (!switcher) return;

  switcher.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      setLanguage(btn.dataset.lang);
    });
  });

  // Apply initial translations
  applyTranslations();
}
